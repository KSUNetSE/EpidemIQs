
# Additional plot: epidemic curves for each scenario, for one representative parameter (e.g., just below and above threshold)
import os
import pandas as pd
import matplotlib.pyplot as plt
# Scenario 1: Poisson(3), random vac, p=0.74 (just below threshold)
s1a = pd.read_csv(os.path.join(os.getcwd(), 'output', 'results-13.csv'))
# Scenario 1: Poisson(3), random vac, p=0.76 (just above threshold)
s1b = pd.read_csv(os.path.join(os.getcwd(), 'output', 'results-14.csv'))
# Scenario 2: Tailored, targeted (all k=10)
s2 = pd.read_csv(os.path.join(os.getcwd(), 'output', 'results-21.csv'))
# Scenario 3: Poisson, targeted (all k=10)
s3 = pd.read_csv(os.path.join(os.getcwd(), 'output', 'results-31.csv'))
# Scenario 4: Tailored, random vac, p=0.74 (just below threshold)
s4a = pd.read_csv(os.path.join(os.getcwd(), 'output', 'results-43.csv'))
# Scenario 4: Tailored, random vac, p=0.76 (just above threshold)
s4b = pd.read_csv(os.path.join(os.getcwd(), 'output', 'results-44.csv'))
plt.figure(figsize=(10,7))
plt.subplot(2,2,1)
plt.plot(s1a['time'], s1a['R'], label='p=0.74 (R, below)')
plt.plot(s1b['time'], s1b['R'], label='p=0.76 (R, above)')
plt.title('Poisson(3) random vac')
plt.xlabel('Time'); plt.ylabel('R'); plt.legend()
plt.subplot(2,2,2)
plt.plot(s2['time'], s2['R'], label='Targeted')
plt.title('Tailored network, all k=10 vaccinated')
plt.xlabel('Time'); plt.ylabel('R'); plt.legend()
plt.subplot(2,2,3)
plt.plot(s3['time'], s3['R'], label='Targeted')
plt.title('Poisson(3) all k=10 vaccinated')
plt.xlabel('Time'); plt.ylabel('R'); plt.legend()
plt.subplot(2,2,4)
plt.plot(s4a['time'], s4a['R'], label='p=0.74 (R, below)')
plt.plot(s4b['time'], s4b['R'], label='p=0.76 (R, above)')
plt.title('Tailored random vac')
plt.xlabel('Time'); plt.ylabel('R'); plt.legend()
plt.tight_layout()
plt.savefig(os.path.join(os.getcwd(), 'output', 'results-summaryepicurves.png'), dpi=150)
